SELECT
    s.dataset,
    s.pipeline,
    s.subject,
    s.session,
    s.n_sessions,
    s.samples,
    s.samples_test,
    s.score_nll,
    s.score_brier,
    s.score_auroc,
    s.score_mcc,
    s.score_ece,
    s.score_mce,
    e.duration,
    e.emissions,
    e.emissions_rate,
    e.cpu_power,
    e.gpu_power,
    e.ram_power,
    e.cpu_energy,
    e.gpu_energy,
    e.ram_energy,
    e.energy_consumed,
    e.country_name,
    e.country_iso_code,
    e.region,
    e.os,
    e.python_version,
    e.codecarbon_version,
    e.cpu_count,
    e.cpu_model,
    e.gpu_count,
    e.gpu_model,
    e.ram_total_size,
    e.tracking_mode
FROM read_csv_auto('{root_dir}/*/*/scores.csv') AS s
INNER JOIN read_csv_auto(
    '{root_dir}/*/*/emissions/emissions_base_*.csv',
    types = { 'python_version': 'VARCHAR', 'codecarbon_version': 'VARCHAR' }
) AS e ON s.codecarbon_task_name = e.task_name
ORDER BY s.dataset, s.pipeline, s.subject, s.session
